import type { Metadata } from "next";
import { Caesar_Dressing, Tillana, Nunito_Sans } from "next/font/google";
import { ThemeProvider as NextJSThemeProvider } from "next-themes";
import "./globals.css";

const caesarDressing = Caesar_Dressing({
  variable: "--font-caesarDressing",
  subsets: ["latin"],
  weight: ["400"],
  display: "swap",
});

const tillana = Tillana({
  variable: "--font-tillana",
  subsets: ["latin"],
  weight: ["400", "500", "600", "700", "800"], 
  display: "swap",
});

const nunitoSans = Nunito_Sans({
  variable: "--font-nunitoSans",
  subsets: ["latin"],
  weight: ["200", "300", "400", "600", "700", "800", "900"], 
  display: "swap",
});

export const metadata: Metadata = {
  title: "Wordsmith",
  description: "Generated by create next app",
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" suppressHydrationWarning={true}>
      <body
        className={`${caesarDressing.variable} ${tillana.variable} ${nunitoSans.variable} antialiased`}
      >
        <NextJSThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
        >
          {children}
        </NextJSThemeProvider>
      </body>
    </html>
  );
}
